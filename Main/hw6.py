# Step 3, 4, 5
def random_selection_func():
    random_selection = []
    for i in range(1, 7, 1):  # Pick random paper
        random_selection.extend([random.choice(paper_container)])
    return random_selection


def calculate_interval(x6, s6):
    critical_value = 2.015
    txs = critical_value * math.sqrt(s6 / 6.0)
    return [float('%0.3f' % (x6 - txs)),
            float('%0.3f' % (x6 + txs))]

# Step 3-6 100 times
results = []
for i in range(1, 101, 1):  # Pick random paper
    random_selection = random_selection_func()
    result = {}
    result['REP'] = i
    result['Random Sample Values'] = random_selection
    result['X(6)'] = float('%0.3f' % (DescStat.mean(random_selection)))
    result['S^2(6)'] = float('%0.3f' % (DescStat.sample_standard_deviation(random_selection)))
    result['Calculate Interval'] = calculate_interval(result['X(6)'],
                                                      result['S^2(6)'])
    if result['X(6)'] >= result['Calculate Interval'][0] and \
            result['X(6)'] <= result['Calculate Interval'][1]:
        result['With in Interval'] = 'Yes'
    else:
        result['With in Interval'] = 'No'
    results.append(result)

